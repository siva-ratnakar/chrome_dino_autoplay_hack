import fs from "node:fs"
import path from "node:path"

function usage() {
  console.log("Usage: node training/train-logistic-model.mjs <input.jsonl> [output.mjs]")
}

const inputPath = process.argv[2]
const outputPath = process.argv[3] || path.join("models", "trained-logistic-model.mjs")

if (!inputPath) {
  usage()
  process.exit(1)
}

if (!fs.existsSync(inputPath)) {
  console.error(`Input file not found: ${inputPath}`)
  process.exit(1)
}

const lines = fs
  .readFileSync(inputPath, "utf8")
  .split(/\r?\n/)
  .map(line => line.trim())
  .filter(Boolean)

if (lines.length === 0) {
  console.error("Dataset is empty")
  process.exit(1)
}

const rows = lines.map(line => JSON.parse(line))

function featureVector(row) {
  return [
    1,
    normalize(row.nearestDistancePx, 220),
    normalize(row.nearestWidthPx, 100),
    normalize(row.nearestHeightPx, 100),
    normalize(row.nearestTimeToImpactMs, 550),
    normalize(row.dinoBottomPx, 120),
    normalize(row.dinoVelocityPxPerMs, 0.35),
    normalize(row.speedScale, 6),
  ]
}

function normalize(value, maxAbs) {
  if (value == null || Number.isNaN(value)) return 0
  return clamp(value / maxAbs, -2.5, 2.5)
}

function clamp(value, min, max) {
  return Math.min(Math.max(value, min), max)
}

const X = rows.map(featureVector)
const y = rows.map(row => (row.label === "jump" ? 1 : 0))

const featureCount = X[0].length
let w = new Array(featureCount).fill(0)

const learningRate = 0.08
const epochs = 240
const l2 = 0.0015

for (let epoch = 0; epoch < epochs; epoch += 1) {
  const grad = new Array(featureCount).fill(0)

  for (let i = 0; i < X.length; i += 1) {
    const xi = X[i]
    const yi = y[i]
    const p = sigmoid(dot(w, xi))
    const error = p - yi

    for (let j = 0; j < featureCount; j += 1) {
      grad[j] += error * xi[j]
    }
  }

  for (let j = 0; j < featureCount; j += 1) {
    const regularized = grad[j] / X.length + l2 * w[j]
    w[j] -= learningRate * regularized
  }
}

const trainMetrics = evaluate(X, y, w)
console.log(`Rows: ${X.length}`)
console.log(`Train accuracy: ${(trainMetrics.accuracy * 100).toFixed(2)}%`)
console.log(`Train precision: ${(trainMetrics.precision * 100).toFixed(2)}%`)
console.log(`Train recall: ${(trainMetrics.recall * 100).toFixed(2)}%`)

const outputSource = `// Auto-generated by training/train-logistic-model.mjs\n// Feature order: bias, distance, width, height, timeToImpact, dinoBottom, dinoVelocity, speedScale\n\nconst weights = ${JSON.stringify(w.map(v => Number(v.toFixed(8))), null, 2)}\n\nfunction clamp(value, min, max) {\n  return Math.min(Math.max(value, min), max)\n}\n\nfunction normalize(value, maxAbs) {\n  if (value == null || Number.isNaN(value)) return 0\n  return clamp(value / maxAbs, -2.5, 2.5)\n}\n\nfunction sigmoid(z) {\n  return 1 / (1 + Math.exp(-z))\n}\n\nfunction score(state) {\n  const nearest = state.obstacles[0]\n  const features = [\n    1,\n    normalize(nearest?.distancePx, 220),\n    normalize(nearest?.widthPx, 100),\n    normalize(nearest?.heightPx, 100),\n    normalize(nearest?.timeToImpactMs, 550),\n    normalize(state.dino.bottomPx, 120),\n    normalize(state.dino.yVelocityPxPerMs, 0.35),\n    normalize(state.speedScale, 6),\n  ]\n\n  let z = 0\n  for (let i = 0; i < weights.length; i += 1) {\n    z += weights[i] * features[i]\n  }\n  return sigmoid(z)\n}\n\nexport default {\n  predict(state) {\n    if (!state.dino.canJump) return { action: \"none\", confidence: 1 }\n    const pJump = score(state)\n    return {\n      action: pJump >= 0.5 ? \"jump\" : \"none\",\n      confidence: Number(pJump.toFixed(4)),\n    }\n  },\n}\n`

fs.mkdirSync(path.dirname(outputPath), { recursive: true })
fs.writeFileSync(outputPath, outputSource, "utf8")
console.log(`Saved model to: ${outputPath}`)

function dot(a, b) {
  let total = 0
  for (let i = 0; i < a.length; i += 1) {
    total += a[i] * b[i]
  }
  return total
}

function sigmoid(z) {
  return 1 / (1 + Math.exp(-z))
}

function evaluate(Xe, ye, weights) {
  let tp = 0
  let tn = 0
  let fp = 0
  let fn = 0

  for (let i = 0; i < Xe.length; i += 1) {
    const p = sigmoid(dot(weights, Xe[i]))
    const pred = p >= 0.5 ? 1 : 0
    const actual = ye[i]

    if (pred === 1 && actual === 1) tp += 1
    if (pred === 0 && actual === 0) tn += 1
    if (pred === 1 && actual === 0) fp += 1
    if (pred === 0 && actual === 1) fn += 1
  }

  const accuracy = (tp + tn) / Xe.length
  const precision = tp + fp === 0 ? 0 : tp / (tp + fp)
  const recall = tp + fn === 0 ? 0 : tp / (tp + fn)

  return { accuracy, precision, recall }
}
